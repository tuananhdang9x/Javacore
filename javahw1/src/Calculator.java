public class Calculator {

    public static int sum(int a, int b) {
        return a + b;
    }

    public static int subtract(int a, int b) {
        return a - b;
    }

    public static double multiply(Double a, Double b) {
        return a * b;
    }

    public static float divide(Float a, Float b) {
        return a / b;
    }

}
